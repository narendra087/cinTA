package domoop;

public class TravelRoomAss {

    int travelRoomID, roomValue;

    public TravelRoomAss(int travelRoomID, int roomValue) {
        this.travelRoomID = travelRoomID;
        this.roomValue = roomValue;
    }

    public int getTravelRoomID() {
        return travelRoomID;
    }

    public void setTravelRoomID(int travelRoomID) {
        this.travelRoomID = travelRoomID;
    }

    public int getRoomValue() {
        return roomValue;
    }

    public void setRoomValue(int roomValue) {
        this.roomValue = roomValue;
    }

}
